package com.jefersonoliveira;

public class Calculadora {
    public int soma (int parcela1, int parcela2){
        return parcela1 + parcela2;
    }    
    public int Subtrair (int parcela1, int parcela2){
        return parcela1 - parcela2;
    }
    public int Multiplica (int parcela1, int parcela2){
        return parcela1 * parcela2;
    }
    public int Dividir (int i, int j){
        return (int) (i / j);
    }
}
